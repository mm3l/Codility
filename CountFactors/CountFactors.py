#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(N):
    """
    CountFactors
    Count factors of given number n.
    """

    i = 1
    result = 0
    
    while i * i < N:
 
        if N % i == 0:
            result += 2
        i += 1

    if i * i == N:
        result += 1

    return result

#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_CountFactors():
    assert solution(24)   == 8

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":
    test_CountFactors()
