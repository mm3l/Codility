#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(A, B, K):
    """
    CountDiv
    Computes number of integers divisible by k in range [a..b].
    """
    
    counter = 0
    for i in range(A, B+1):
        if (i % K) == 0:
            counter += 1

    return counter

#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_MinAvgTwoSlice():
   assert solution(6, 11, 2)    == 3
   assert solution(0, 5, 2)     == 3
   assert solution(0, 0, 11)    == 1
   assert solution(0, 1, 11)    == 1

