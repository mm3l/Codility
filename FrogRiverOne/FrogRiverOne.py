#-----------------------------------------------------------------------------#
#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(X, A):
    """
    Finds the earliest time when a frog can jump to the other side of a river.
    """

    steps = {}
    how_many_leafs = -1
    for i in range(len(A)):
        
        if A[i] <= X:
            steps[A[i]] = i

        if len(steps) == X:
            how_many_leafs = i
            break

    return how_many_leafs    
    
#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_FrogRiverOne():
    assert solution(5, [1,3,1,4,2,3,5,4]) == 6
    assert solution(1, [1])               == 0
    assert solution(2, [1,2])             == 1
    assert solution(2, [2, 2, 2, 2, 2])   == -1

    assert solution(7, [5,19,2,33,1,6,9,8,4,3,66,7,23,55,34])   == 11

