#-----------------------------------------------------------------------------#

#-----------------------------------------------------------------------------#
#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(X, Y, D):
    """ Counts minimal number of jumps from position X to Y."""

    result = (Y - (X + 1)) // D

    return (result + 1)

#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_Frogjmp():
    assert solution(10, 85, 30) == 3
    assert solution(10, 90, 30) == 3
    assert solution(1, 5, 2) == 2
    assert solution(2, 5, 2) == 2

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":

    test_Frogjmp()
