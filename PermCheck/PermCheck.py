#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(A):
    """
    Checks whether array A is a permutation
    """
    
    check = [0] * (len(A) + 1)
    counter = 0

    for i in range(len(A)):
        value = A[i]
        if (value >= len(check)):
            return 0
        if check[value] == 0:
            check[value] = 1
            counter += 1
        else:
            return 0

    return (1 if counter == len(A) else 0)
    
#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_PermCheck():
    assert solution([4,1,3,2]) == 1
    assert solution([4,1,3])   == 0
    assert solution([2])       == 0

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":
    test_PermCheck()
