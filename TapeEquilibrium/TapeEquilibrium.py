#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(A):
    """
    TapeEquilibrium
    Minimize the value |(A[0] + ... + A[P-1]) - (A[P] + ... + A[N-1])|.
    """

    arr_sum = sum(A)
    l_count = 0
    distance = float('inf')

    for i in range(0, len(A)-1):
        l_count += A[i]
        distance = min(abs(arr_sum - 2 * l_count), distance)
    
    return distance
    
#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_TapeEquilibrium():
    assert solution([3,1,2,4,3]) == 1

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":

    test_TapeEquilibrium()
