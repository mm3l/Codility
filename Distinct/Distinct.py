#-----------------------------------------------------------------------------#
#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(A):
    """
    Distinct
    Compute number of distinct values in an array.
    """

    N = len(A)
    histo = dict()

    for i in range(N):
        histo[A[i]] = 1

    return len(histo)

#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_Distinct():
   assert solution([2,2,1,3,1,1])     == 3
   assert solution([4,2,1,3,1,1])     == 4

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":
    test_Distinct()
