#-----------------------------------------------------------------------------#
#                                 Solution                                    #
#-----------------------------------------------------------------------------#
def solution(A):
    """ 
    Find value that occurs in odd number of elements 
    Time complexity: O(N)
    """
    
    result = 0

    for number in A:
        result ^= number

    return result

#-----------------------------------------------------------------------------#
#                                 TEST                                        #
#-----------------------------------------------------------------------------#
def test_OddOccurrencesInArray():
    assert solution([9,3,9,3,9,7,9]) == 7
    assert solution([1,2,3,8,3,2,1]) == 8
    assert solution([9,9,8,1,8,9,9]) == 1

#-----------------------------------------------------------------------------#
#                                 MAIN                                        #
#-----------------------------------------------------------------------------#

if __name__ == "__main__":

    test_OddOccurrencesInArray()
